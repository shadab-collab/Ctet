async function downloadLeaderboard() {
    
    const res = await fetch("/api/results");
    
    const results = await res.json();
    
    if (results.length === 0) {
      
      alert("Leaderboard Empty");
      
      return;
      
    }
    
    results.sort((a, b) => b.score - a.score);
    
    const today = new Date().toLocaleDateString("en-GB");
    
    const quizTitle = document
      .getElementById("quizTitle")
      .value || "CTET LIVE QUIZ";
    
    let html = `

<div id="exportCard"

style="

width:900px;

background:white;

padding:40px;

font-family:Arial;

">

<div

style="

background:linear-gradient(90deg,#1565c0,#42a5f5);

color:white;

padding:25px;

border-radius:15px;

text-align:center;

">

<h1>

🏆 CTET LIVE QUIZ

</h1>

<h2>

${quizTitle}

</h2>

<p>

📅 ${today}

</p>

<p>

👨‍🎓 Participants :

${results.length}

</p>

</div>

<br>

<table

style="

width:100%;

border-collapse:collapse;

">

<tr

style="

background:#1565c0;

color:white;

">

<th>

Rank

</th>

<th>

Student

</th>

<th>

Score

</th>

</tr>

`;
    
    results.forEach((r, index) => {
      
      let medal = index + 1;
      
      if (medal == 1)
        
        medal = "🥇";
      
      else if (medal == 2)
        
        medal = "🥈";
      
      else if (medal == 3)
        
        medal = "🥉";
      
      else
        
        medal = index + 1;
      
      html += `

<tr>

<td

style="

padding:12px;

border:1px solid #ddd;

text-align:center;

">

${medal}

</td>

<td

style="

padding:12px;

border:1px solid #ddd;

">

${r.name}

</td>

<td

style="

padding:12px;

border:1px solid #ddd;

text-align:center;

font-weight:bold;

">

${r.score}

</td>

</tr>

`;
      
    });
        html += `

</table>

<br><br>

<div
style="

background:#f5f5f5;

padding:20px;

border-radius:12px;

text-align:center;

">

<h2
style="

color:#1565c0;

margin:0;

">

🏆 Congratulations To All Participants 🏆

</h2>

<p
style="

font-size:18px;

margin-top:10px;

color:#444;

">

Keep Learning • Keep Growing • Keep Winning

</p>

</div>

<br>

<div
style="

text-align:center;

font-size:15px;

color:#777;

border-top:2px solid #ddd;

padding-top:15px;

">

Generated by

<b>CTET LIVE QUIZ</b>

</div>

</div>

`;
    
    const box = document.createElement("div");
    
    box.innerHTML = html;
    
    box.style.position = "fixed";
    
    box.style.left = "-10000px";
    
    box.style.top = "0";
    
    document.body.appendChild(box);
        const card = document.getElementById("exportCard");
    
    html2canvas(card, {
      
      scale: 3,
      
      useCORS: true,
      
      backgroundColor: "#ffffff"
      
    }).then(canvas => {
      
      const link = document.createElement("a");
      
      const today = new Date()
        .toISOString()
        .slice(0, 10);
      
      link.download =
        "CTET-Leaderboard-" + today + ".png";
      
      link.href =
        canvas.toDataURL("image/png");
      
      link.click();
      
      document.body.removeChild(box);
      
    }).catch(err => {
      
      console.log(err);
      
      alert("Image Export Failed");
      
      document.body.removeChild(box);
      
    });
    
    }